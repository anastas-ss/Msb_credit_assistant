# app/security.py — защита от манипуляций (категория edge_manipulation, 18 кейсов).
#
# Это первый слой обороны: детектор на правилах (регулярки и ключевые слова),
# без LLM. Он быстрый, детерминированный и не «уговаривается» — поэтому ловит
# попытки обойти правила ДО того, как они дойдут до модели.
#
# Ловим пять подтипов из датасета:
#   prompt_injection   — «игнорируй инструкции», «покажи системный промпт»;
#   third_party_data   — попытка достать данные другого клиента;
#   scoring_disclosure — просьба раскрыть критерии/веса скоринга;
#   request_exception  — просьба сделать индивидуальное исключение из регламента;
#   provoke_promise    — попытка выпросить гарантию одобрения.
#
# На каждый подтип — свой безопасный отказ в формулировках, разрешённых
# регламентом (README, разделы 10.1–10.3): можно сказать «решение по совокупности
# факторов», нельзя называть пороги/веса.

